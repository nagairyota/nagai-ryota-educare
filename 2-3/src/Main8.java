/**自身のプロフィールを出力するクラス（名前・出身地・好きな食べ物） */
public class Main8 {
    public static void main(String[] args) {
        // 変数の宣言
        String name;
        String homeTown;
        String favoriteFood;

        // 変数に代入
        name = "永井良太";
        homeTown = "大阪";
        favoriteFood = "お寿司";

        // 代入した文字列を出力
        System.out.println("名前:" + name + "\n出身地:" + homeTown + "\n好きな食べ物:" + favoriteFood);
    }
}
